            'products' => Product::join('brands','products.brand_id', '=','brands.id_brands')
                    ->join('units', 'products.unit_id','=','units.id_unit')
                    ->orderby('products.name_product','asc')->get(),
        );
        return view('pages.transactions.add', $data);
